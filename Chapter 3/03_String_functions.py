print(len(name)) # It will give the length of the string.
print(name.endswith("yan")) # It will give True of False wheather to check string ends with your letter.
print(name.startswith("ar")) # Same like endswith but for Start. Letter should be capital if Starting from captial same goes for end.
print(name.capitalize()) # It will make first letter capital.

